function levelDrawer(levelName = levelOne) {
  board.innerHTML = '';
  for (let i = 0; i < 100; i++) {
    const element = document.createElement('div');
    element.classList.add('grid');
    element.setAttribute('id', i);
    if (levelName[i] == 1) {
      element.classList.add('wall');
    }
    board.appendChild(element);
  }
}

